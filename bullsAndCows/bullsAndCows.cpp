#include <iostream>

int main()
{
    setlocale(LC_ALL, "RUS");
    std::cout << "4. «Быки и коровы» (дополнительное задание)\n\n";

    std::string number1;  // задуманное число
    std::cout << "Enter the intended number: ";
    std::cin >> number1;

    std::string number2;  // второе число
    std::cout << "Enter the second number: ";
    std::cin >> number2;

    int cow = 0 ;   // счетчик коров
    int bull = 0 ;  // счетчик быков

    // считаем коров
    int start = 0;
    for ( ; start < number1.length(); start++)
    {
       
        for (int i = 0; i < number2.length(); i++)
        {
            if (number1[start] == number2[i] && start != i)
            {
                if ((number1[i] != number2[i]) && number1[start] != number2[start])
                {
                    cow++;
                    start++;
                    break;
                }
            }
        }
    }

    // считаем быков
    for (int s = 0; s < number1.length(); s++)
    {
        if (number1[s] == number2[s])
        {
            bull++;
        }
    }
    std::cout << "bulls: " << bull << " cows: " << cow;


    return 0;
}

